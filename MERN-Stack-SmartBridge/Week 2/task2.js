//  Given x = 10 and y = 5, perform the following operations and store the results: • Addition • Multiplication • Modulus 
let x = 10, y = 5;

// Performing operations and storing results
let sum = x + y;          // Addition
let product = x * y;      // Multiplication
let remainder = x % y;    // Modulus

// Displaying results
console.log(`Sum: ${sum}`);
console.log(`Product: ${product}`);
console.log(`Remainder: ${remainder}`);